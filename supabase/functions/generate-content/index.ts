import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { prompt, type } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    let systemPrompt = "";
    
    if (type === "blog-content") {
      systemPrompt = `You are a skilled technical blog writer. Write engaging, well-structured blog content based on the user's topic or prompt. 
Use markdown formatting with:
- Clear headings (##, ###)
- Code blocks with syntax highlighting when relevant
- Bullet points for lists
- Bold and italic for emphasis
- Keep paragraphs concise and readable
Write in a professional yet conversational tone. Include practical examples and insights.`;
    } else if (type === "excerpt") {
      systemPrompt = `You are a skilled copywriter. Generate a compelling blog post excerpt/summary in 2-3 sentences (max 160 characters). Make it engaging and capture the essence of the content. Do not use markdown.`;
    } else if (type === "improve") {
      systemPrompt = `You are an expert editor. Improve the given content by:
- Fixing grammar and spelling
- Improving clarity and flow
- Enhancing structure
- Making it more engaging
Keep the same general style and format. Return only the improved content in markdown.`;
    } else {
      systemPrompt = `You are a helpful AI assistant. Respond to the user's request clearly and concisely.`;
    }

    console.log(`Generating ${type} content with prompt: ${prompt.substring(0, 100)}...`);

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: prompt },
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add more credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      throw new Error(`AI gateway error: ${response.status}`);
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      throw new Error("No content generated");
    }

    console.log("Content generated successfully");

    return new Response(JSON.stringify({ content }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Error in generate-content:", error);
    const errorMessage = error instanceof Error ? error.message : "Unknown error";
    return new Response(JSON.stringify({ error: errorMessage }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
